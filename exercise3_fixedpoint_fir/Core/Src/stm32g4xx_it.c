/**
  * @brief This function handles ADC1 and ADC2 global interrupt.
  */
void ADC1_2_IRQHandler(void)
{
  /* USER CODE BEGIN ADC1_2_IRQn 0 */

  /* USER CODE END ADC1_2_IRQn 0 */
  HAL_ADC_IRQHandler(&hadc1);
  /* USER CODE BEGIN ADC1_2_IRQn 1 */

	// verifies the correct sample frequency of the ADC
	// HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_9);
	// Reads the current value of the ADC as int16
	int16_t input_raw = HAL_ADC_GetValue(&hadc1); // 0â€¦4095

	//Scales the 8 bit ADC value to the Q15 format
//	int16_t input_q15 = (int16_t) (((int32_t) input_raw - ADC_RANGE_HALF)
//			* Q15_SCALE / ADC_RANGE_HALF);

	// Rising edge, to start the measurement
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_SET);

	// Filters the input signal using the floating point arithmetic
	// float y_filtered = ema_filter(input_raw);

	// Filters the input signal using the fixed point arithmetic
	// int16_t y_filtered = ema_filter_q15(input_q15);

	// Filters the signal using a FIR filter
	float y_filtered = fir_filter((float) input_raw - 2048.f);

	// Falling edge, to stop the measurement
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, GPIO_PIN_RESET);

	//uint16_t dac_filtered = ((int32_t) y_filtered + Q15_SCALE) * ADC_RANGE
	//	/ Q15_RANGE;

	// Output of raw Signal on PA4
	HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_1, DAC_ALIGN_12B_R, input_raw);

	// Output of filtered Signal on PA5
	HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_2, DAC_ALIGN_12B_R,
			(uint16_t) (y_filtered + 2048));
  /* USER CODE END ADC1_2_IRQn 1 */
}

