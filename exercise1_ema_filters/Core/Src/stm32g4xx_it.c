/**
 * @brief This function handles TIM6 global interrupt, DAC1 and DAC3 channel underrun error interrupts.
 */
void TIM6_DAC_IRQHandler(void) {
	/* USER CODE BEGIN TIM6_DAC_IRQn 0 */
	HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_6);
	/* USER CODE END TIM6_DAC_IRQn 0 */
	HAL_TIM_IRQHandler(&htim6);
	HAL_DAC_IRQHandler(&hdac1);
	/* USER CODE BEGIN TIM6_DAC_IRQn 1 */
	float y = ema_filter(adc_values[sample_index], 0.5);

	// Sets the input signal
	HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_1, DAC_ALIGN_12B_R,
			adc_values[sample_index]);

	// Sets the output signal (low pass filter)
	//HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_2, DAC_ALIGN_12B_R, (uint16_t) y);

	// Sets the output signal (high pass filter)
	HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_2, DAC_ALIGN_12B_R,
			(uint16_t) (adc_values[sample_index] - y));

	sample_index++;
	if (sample_index >= 1024)
		sample_index = 0;
	/* USER CODE END TIM6_DAC_IRQn 1 */
}

